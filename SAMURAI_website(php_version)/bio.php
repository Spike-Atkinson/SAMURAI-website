<h1 class="bio" id="bio-h1"> Bio </h1>
<p class="bio" >Samurai started as a back-street band, playing in various bars and clubs around Night City, such as 
the Palladium, Destiny, Nexus or Rainbow Cadenza. One night in 2003, after playing in the Cadenza, the band was approached by a blitzed 'dorpher, 
who offered to sign them after witnessing their impressive act; this man turned out to be none other than Jack Masters, an executive at Universal Music. 
Within three weeks, Samurai was signed, had a record in production, and was preparing to tour every major city in the world. 
It took only these three weeks for their first single, "Blistering Love", to achieve #1 hit on the EuroRadio charts, 
which they followed up with their debut album.</p>
<p class="bio">Samurai split up in 2008 when Nancy (keyboardist) went to jail for seven months for throwing her abusive husband out of their 83rd story apartment window. 
The rest of the band firmly supported Nancy, but the seven months were proof that the members were all moving in different creative directions. 
Henry (bassist) went to work on a human interface prototype, and Denny (drummer) joined a band called Mastermind. 
Nancy later changed her name to Bes Isis, and went on to become a major media presence on N54 News. Johnny Silverhand and Kerry Eurodyne went on to have 
successful solo careers, often touring together as a double ticket.</p>
 <p class="bio">Despite the band being named Samurai, the logo is actually that of an "Oni" 
(Japanese: 鬼 or おに, meaning "ogre, demon") which are monstrous horned humanoids who are found in countless Japanese stories and myths.
They tend to be the enemies of mankind, and are generally depicted as roguish villains.<sup>[1]</sup> </p>

